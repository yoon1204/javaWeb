package Board;


public class Board {
	
	String id;
	String no;
	String contents;
	String memo;
	String date;
	String star;
	String finished;
	
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getStar() {
		return star;
	}
	public void setStar(String star) {
		this.star = star;
	}
	public String getFinished() {
		return finished;
	}
	public void setFinished(String finished) {
		this.finished = finished;
	}
	
	public Board() {
		// TODO Auto-generated constructor stub
	}
	
	public Board(String id, String contents, String memo, String date, String star, String finished) {
		super();
		this.id = id;
		this.no = no;
		this.contents = contents;
		this.memo = memo;
		this.date = date;
		this.star = star;
		this.finished = finished;
	}
	@Override
	public String toString() {
		return "Board [id=" + id + ", no=" + no + ", contents=" + contents + ", memo=" + memo + ", date=" + date
				+ ", star=" + star + ", finished=" + finished + "]";
	}
	
	
	
	
	
	
}
